package com.javaintro;
//operators
public class LogiOper1 {

	public static void main(String[] args) {
	int a = 44;
	int b = 52;
	int c = 2;
	System.out.println(a<b&&a>b);
	System.out.println(!(a<b||a>b));
	System.out.println(a<b&a>b);
	System.out.println(a<b|a>b);
	System.out.println(a^b);
	System.out.println(~b);
	System.out.println(a<<c);
	System.out.println(a>>b);
	

	}

}
